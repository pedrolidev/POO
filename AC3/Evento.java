public class Evento {

    String nome, data, local, info;
    int maxParticipantes;

    Evento(String nome, String data, String local, String info, int maxParticipantes) {
        this.nome = nome;
        this.data = data;
        this.local = local;
        this.info = info;
        this.maxParticipantes = maxParticipantes;
    }

}